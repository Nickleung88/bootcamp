public class Animal {
  private int age;
    private String color;

  public Animal() {

  }

  public Animal(int age) {
    this.age = age;
  }

  public int getAge() {
    return this.age;
  }

  public void setAge(int age) {
    this.age = age;
  }

  public Animal(String color) {
    this.color = color;
  }

  public String getColor() {
    return this.color;
  }

  public void setColor(String color){
    this.color=color;
  }
}
